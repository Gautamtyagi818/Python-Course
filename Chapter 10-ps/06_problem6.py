# Chaange the self into slf nd see the effects

class programmer:
    company = "Microsoft" 
    def __init__(slf, name, salary, pin):
        slf.name = name 
        slf.salary = salary
        slf.pin = pin 
        
p = programmer("Harry", 12000, 250001,)
print(p.name, p.salary, p.pin, p.company) 
r = programmer("Rohan", 12000, 250001,)
print(r.name, r.salary, r.pin, r.company) 